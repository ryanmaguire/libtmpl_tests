#ifndef TMPL_TESTS_MPF_GET_LD_H
#define TMPL_TESTS_MPF_GET_LD_H

#include <string.h>
#include <gmp.h>

static long double tmpl_tests_mpf_get_ld(mpf_t op)
{
    char buffer[128];
    gmp_sprintf(buffer, "%.100FE", op);
    return strtold(buffer, NULL);
}

#endif
